let StringParaNumero = "100";
console.log(StringParaNumero); // Saída: "100"
console.log(typeof(StringParaNumero)); // Saída: String
console.log(""); // Saída: "100"
// Conversão da String "StringParaNumero" para Number
let numero = parseInt(StringParaNumero);
console.log(numero); // Saída: 100
console.log(typeof(numero)); // Saída: Number